void clear_edit_input(void)
{
    edit_digit_count = 0;
    edit_digits[0] = 0;
    edit_digits[1] = 0;
    edit_digits[2] = 0;
    edit_digits[3] = 0;
}

void next_edit_field(void)
{
    clear_edit_input();
    edit_field++;
    if (edit_field > EDIT_YEAR) edit_field = EDIT_HOUR;
}

void apply_edit_value(void)
{
    unsigned int value = 0;
    unsigned char i;
    unsigned char new_day = day;
    unsigned char new_month = month;
    unsigned int new_year = year;

    for (i = 0; i < edit_digit_count; i++) {
        value = value * 10 + edit_digits[i];
    }

    if (edit_field == EDIT_HOUR && edit_digit_count == 2 && value < 24) {
        hour = (unsigned char)value;
        second = 0;
    } else if (edit_field == EDIT_MINUTE && edit_digit_count == 2 && value < 60) {
        minute = (unsigned char)value;
        second = 0;
    } else if (edit_field == EDIT_DAY && edit_digit_count == 2) {
        new_day = (unsigned char)value;
        if (valid_date(new_day, month, year)) day = new_day;
    } else if (edit_field == EDIT_MONTH && edit_digit_count == 2) {
        new_month = (unsigned char)value;
        if (valid_date(day, new_month, year)) month = new_month;
    } else if (edit_field == EDIT_YEAR && edit_digit_count == 4) {
        new_year = value;
        if (valid_date(day, month, new_year)) year = new_year;
    }
}

void handle_digit(unsigned char digit)
{
    unsigned char max_digits;

    if (!edit_mode) return;

    max_digits = (edit_field == EDIT_YEAR) ? 4 : 2;
    if (edit_digit_count >= max_digits) {
        clear_edit_input();
    }

    edit_digits[edit_digit_count] = digit;
    edit_digit_count++;

    if (edit_digit_count == max_digits) {
        apply_edit_value();
    }

    need_redraw = 1;
}

void keypad_rows_idle(void)
{
    PORTC = 0x70;
    PORTE = 0x03;
    PORTD = 0x81;
}

unsigned char scan_keypad_raw(void)
{
    unsigned char r;

    for (r = 0; r < 3; r++) {
        if (r == 0) {
            PORTC = 0x30;
            PORTE = 0x03;
            PORTD = 0x81;
        }

        if (r == 1) {
            PORTC = 0x70;
            PORTE = 0x01;
            PORTD = 0x81;
        }

        if (r == 2) {
            PORTC = 0x70;
            PORTE = 0x03;
            PORTD = 0x80;
        }

        delay_us(10);

        if ((PINC & 0x10) == 0) {
            keypad_rows_idle();
            return keypad_value[r][0];
        }

        if ((PINC & 0x20) == 0) {
            keypad_rows_idle();
            return keypad_value[r][1];
        }

        if ((PINE & 0x01) == 0) {
            keypad_rows_idle();
            return keypad_value[r][2];
        }
    }

    keypad_rows_idle();
    return 0;
}

unsigned char debounce_state(unsigned char raw,
                             unsigned char *stable,
                             unsigned char *last_raw,
                             unsigned char *count)
{
    if (raw == *last_raw) {
        if (*count < DEBOUNCE_TICKS) (*count)++;
    } else {
        *last_raw = raw;
        *count = 0;
    }

    if (*count >= DEBOUNCE_TICKS && *stable != raw) {
        *stable = raw;
        if (raw != 0) {
            return 1;
        }
    }

    return 0;
}

void timer0_poll_keys(void)
{
    static unsigned char bt1_stable = 0;
    static unsigned char bt1_last = 0;
    static unsigned char bt1_count = 0;
    static unsigned char bt2_stable = 0;
    static unsigned char bt2_last = 0;
    static unsigned char bt2_count = 0;
    static unsigned char bt3_stable = 0;
    static unsigned char bt3_last = 0;
    static unsigned char bt3_count = 0;
    static unsigned char key_stable = 0;
    static unsigned char key_last = 0;
    static unsigned char key_count = 0;
    static unsigned int bt2_hold_ms = 0;
    static unsigned char bt2_hold_sent = 0;
    unsigned char raw_key;

    if (debounce_state((BT1_MODE == BTN_PRESSED), &bt1_stable, &bt1_last, &bt1_count)) {
        bt1_event = 1;
    }

    if (debounce_state((BT3_ZERO == BTN_PRESSED), &bt3_stable, &bt3_last, &bt3_count)) {
        bt3_event = 1;
    }

    debounce_state((BT2_EDIT == BTN_PRESSED), &bt2_stable, &bt2_last, &bt2_count);
    if (bt2_stable) {
        if (!bt2_hold_sent) {
            bt2_hold_ms += POLL_MS;
            if (bt2_hold_ms >= BT2_HOLD_MS) {
                bt2_hold_event = 1;
                bt2_hold_sent = 1;
            }
        }
    } else {
        if (bt2_hold_ms > 0 && !bt2_hold_sent) {
            bt2_short_event = 1;
        }
        bt2_hold_ms = 0;
        bt2_hold_sent = 0;
    }

    raw_key = scan_keypad_raw();
    if (debounce_state(raw_key, &key_stable, &key_last, &key_count)) {
        if (!keypad_event) {
            keypad_event_value = raw_key;
            keypad_event = 1;
        }
    }
}

void handle_bt1(void)
{
    if (bt1_event) {
        #asm("cli")
        bt1_event = 0;
        #asm("sei")
        if (!edit_mode) {
            display_mode = !display_mode;
            need_redraw = 1;
        }
    }
}

void handle_bt3_zero(void)
{
    if (bt3_event) {
        #asm("cli")
        bt3_event = 0;
        #asm("sei")
        handle_digit(0);
    }
}

void handle_bt2_edit(void)
{
    if (bt2_hold_event) {
        #asm("cli")
        bt2_hold_event = 0;
        bt2_short_event = 0;
        #asm("sei")
        edit_mode = !edit_mode;
        clear_edit_input();
        edit_field = EDIT_HOUR;
        need_redraw = 1;
        return;
    }

    if (bt2_short_event) {
        #asm("cli")
        bt2_short_event = 0;
        #asm("sei")
        if (edit_mode) {
            next_edit_field();
            need_redraw = 1;
        }
    }
}

