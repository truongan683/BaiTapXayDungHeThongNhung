void glcd_start(void)
{
    GLCDINIT_t glcd_init_data;

    glcd_init_data.font = font5x7;
    glcd_init_data.temp_coef = 80;
    glcd_init_data.bias = 3;
    glcd_init_data.vlcd = 55;
    glcd_init(&glcd_init_data);
    glcd_clear();
}

void main(void)
{
    unsigned char key;

    io_init();
    codewizard_peripheral_init();
    glcd_start();
    timer1_init();
    timer0_init();

    #asm("sei")

    draw_screen();

    while (1) {
        while (tick_1s) {
            #asm("cli")
            tick_1s--;
            #asm("sei")
            if (!edit_mode) {
                increase_one_second();
            }
            need_redraw = 1;
        }

        handle_bt1();
        handle_bt2_edit();
        handle_bt3_zero();

        if (keypad_event) {
            #asm("cli")
            key = keypad_event_value;
            keypad_event = 0;
            #asm("sei")
            handle_digit(key);
        }

        if (need_redraw) {
            need_redraw = 0;
            draw_screen();
        }
    }
}
