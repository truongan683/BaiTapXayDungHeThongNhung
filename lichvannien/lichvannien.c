unsigned char is_leap_year(unsigned int y)
{
    if ((y % 400) == 0) return 1;
    if ((y % 100) == 0) return 0;
    return ((y % 4) == 0);
}

unsigned char days_in_month(unsigned char m, unsigned int y)
{
    switch (m) {
    case 1:
    case 3:
    case 5:
    case 7:
    case 8:
    case 10:
    case 12:
        return 31;
    case 4:
    case 6:
    case 9:
    case 11:
        return 30;
    case 2:
        return is_leap_year(y) ? 29 : 28;
    default:
        return 31;
    }
}

unsigned char valid_date(unsigned char d, unsigned char m, unsigned int y)
{
    if (y < 2000 || y > 2099) return 0;
    if (m < 1 || m > 12) return 0;
    if (d < 1 || d > days_in_month(m, y)) return 0;
    return 1;
}

long date_to_days(unsigned int y, unsigned char m, unsigned char d)
{
    long total = 0;
    unsigned int yy;
    unsigned char mm;

    if (y >= 2000) {
        for (yy = 2000; yy < y; yy++) {
            total += is_leap_year(yy) ? 366 : 365;
        }
    } else {
        for (yy = y; yy < 2000; yy++) {
            total -= is_leap_year(yy) ? 366 : 365;
        }
    }

    for (mm = 1; mm < m; mm++) {
        total += days_in_month(mm, y);
    }

    total += d - 1;
    return total;
}

void increase_one_second(void)
{
    second++;
    if (second < 60) return;
    second = 0;

    minute++;
    if (minute < 60) return;
    minute = 0;

    hour++;
    if (hour < 24) return;
    hour = 0;

    day++;
    if (day <= days_in_month(month, year)) return;
    day = 1;

    month++;
    if (month <= 12) return;
    month = 1;
    year++;
}

LunarDate get_lunar_date(unsigned int sy, unsigned char sm, unsigned char sd)
{
    LunarDate out;
    long current;
    long start;
    unsigned int i;

    out.valid = 0;
    out.day = 0;
    out.month = 0;
    out.year = 0;
    out.leap = 0;

    current = date_to_days(sy, sm, sd);

    for (i = 0; i < LUNAR_TABLE_SIZE; i++) {
        start = date_to_days(lunar_table[i].solar_year,
                             lunar_table[i].solar_month,
                             lunar_table[i].solar_day);

        if (current >= start &&
            current < (start + lunar_table[i].month_days)) {
            out.valid = 1;
            out.day = (unsigned char)(current - start + 1);
            out.month = lunar_table[i].lunar_month;
            out.year = lunar_table[i].lunar_year;
            out.leap = lunar_table[i].leap;
            return out;
        }
    }

    return out;
}

